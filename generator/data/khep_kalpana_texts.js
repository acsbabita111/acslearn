  /* 163 घर निर्माण */ "घर बनवाने का अपना ठेका और नींव पर अपना नाम — यह राज-मालिकी चाहोगे?",
  /* 164 अपार्टमेंट */ "अपार्टमेंट-परियोजना में अपनी बिल्डर-फ़र्म की तख़्ती — यह ऊँचा सपना जँचेगा?",
  /* 165 वाणिज्यिक संपत्ति */ "दुकानों वाली अपनी व्यावसायिक इमारत का किराया-खाता — यह पक्की कमाई भाएगी?",
