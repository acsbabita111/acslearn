  /* 88 संपीडित/पाइपलाइन गैस */ "गाड़ियों के लिए गैस-पंप की अपनी फ़्रैंचाइज़ी लेना — यह भरा-पूरा सपना जँचेगा?",
  /* 89 इथेनॉल/जैव ईंधन */ "इथेनॉल बनाने वाली अपनी छोटी इकाई का लाइसेंस पाना — यह हरा ईंधन भाएगा?",
