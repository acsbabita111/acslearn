  /* 128 मुद्रण मशीनरी */ "छपाई-मशीनों की मरम्मत वाली अपनी वर्कशॉप — यह स्याही-हुनर भाएगा?",
  /* 129 पंप एवं मोटर */ "पंप-मोटर की अपनी दुकान और खेतों में अपनी फिटिंग-टीम — मालिक बनोगे?",
