  /* 91 कोयला खनन सेवाएं */ "खदान-सेवा की अपनी मशीन-टोली और अपने ठेके — यह दम-खम धंधा जँचेगा?",
  /* 92 तेल कुआं सेवाएं */ "तेल-कुएँ की मरम्मत-टोली का अपना ठेकेदार बनना — यह कड़ी मेहनत भाएगी?",
